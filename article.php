window.onload = function(){ 
  // กรอบรูป

      var frame = $( '.art_fram_img' ).width();
      // ถ้าเป็นรูป4เหลี่ยมไม่ต้องใช้รูปนี้เพิ่ม
      frame = frame / 1.5; 
      $('.art_fram_img').css({'height':frame + 'px'}); 


 // รูป
       $( '.art_img-card' ).each( function () { 
           var iw = $(this).width(); 
           var ih = $(this).height(); 
           if(iw > ih){ 
             $(this).css({'width':'auto'}); 
             $(this).css({'height':100+'%'}); 
           } 
           else if(ih > iw){ 
             $(this).css({'width':100+'%'}); 
             $(this).css({'height':'auto'}); 
           } 
           else if(ih == iw){ 
             $(this).css({'width':100+'%'}); 
             $(this).css({'height':'auto'}); 
           } 
        }); 
 
} 
 
</script>